/**
 * Parser for cards-timeline. Base: cards.
 * Library convention: cards container — one row per card (item); cells carry the item's model
 *   fields as rich text/image. This variant's model adds a `year` field ahead of image + text.
 * Source: https://www.suze.com/suze-story/ (section 2) — 10 timeline entries (years 1885..2003).
 * Model (blocks/cards-timeline/_cards-timeline.json): container "cards-timeline" with child
 *   "card" (Timeline Entry) items. card fields (in order): year (text), image (reference) +
 *   imageAlt (collapsed), text (richtext).
 * The year-navigation list is GENERATED by decorate() from the entry headings — NOT authored
 *   content, so the source nav (.historySection__nav) is excluded.
 * Structure: one row per entry, 3 columns following the model field order:
 *   col 1 = year label (field:year)
 *   col 2 = image (field:image)
 *   col 3 = richtext (field:text): H2 heading (year) + paragraph.
 */
export default function parse(element, { document }) {
  const rows = Array.from(element.querySelectorAll('.historySection__content__item__row, .historySection__content > div'));

  // Empty-block guard
  if (rows.length === 0) {
    element.replaceWith(...element.childNodes);
    return;
  }

  const cells = [];

  rows.forEach((row) => {
    const image = row.querySelector('.historySection__content__item__col__img img, .col-img img, img');
    const textCol = row.querySelector('.historySection__content__item__col__text, .col-text');
    const scope = textCol || row;
    const heading = scope.querySelector('h1, h2, h3, h4');
    const para = scope.querySelector('p');
    const yearLabel = heading ? heading.textContent.trim() : '';

    // Skip malformed rows (e.g. stray wrappers with neither image nor text)
    if (!image && !heading && !para) return;

    // col 1: year (field:year)
    const yearFrag = document.createDocumentFragment();
    yearFrag.appendChild(document.createComment(' field:year '));
    if (yearLabel) yearFrag.appendChild(document.createTextNode(yearLabel));

    // col 2: image (field:image; imageAlt collapses into <img alt>)
    const imageFrag = document.createDocumentFragment();
    if (image) {
      imageFrag.appendChild(document.createComment(' field:image '));
      imageFrag.appendChild(image);
    }

    // col 3: text richtext (field:text) — H2 heading + paragraph
    const textFrag = document.createDocumentFragment();
    textFrag.appendChild(document.createComment(' field:text '));
    if (heading) textFrag.appendChild(heading.cloneNode(true));
    if (para) textFrag.appendChild(para.cloneNode(true));

    cells.push([yearFrag, imageFrag, textFrag]);
  });

  const block = WebImporter.Blocks.createBlock(document, { name: 'cards-timeline', cells });
  element.replaceWith(block);
}
